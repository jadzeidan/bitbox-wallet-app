func TestSealAndUnsealSecretsEncrypted(t *testing.T) {
	env := &testEnvironment{canEncrypt: true}
	lightning := newTestLightning(t, env)

	sealedMnemonic, sealedPassword, err := lightning.sealSecrets("v0-deadbeef-ln-0", "test mnemonic", "test password")
	require.NoError(t, err)
	require.NotEqual(t, "test mnemonic", sealedMnemonic)
	require.NotEqual(t, "test password", sealedPassword)
	require.True(t, strings.HasPrefix(sealedMnemonic, encryptedSecretV1Prefix))
	require.True(t, strings.HasPrefix(sealedPassword, encryptedSecretV1Prefix))
	require.Len(t, env.keys, 1)

	account := &config.LightningAccountConfig{
		Code:           "v0-deadbeef-ln-0",
		Seed:           sealedMnemonic,
		WalletPassword: sealedPassword,
	}

	mnemonic, err := lightning.unsealSecret(account, account.Seed)
	require.NoError(t, err)
	require.Equal(t, "test mnemonic", mnemonic)
	require.Equal(t, 1, env.loadCalls)

	password, err := lightning.unsealSecret(account, account.WalletPassword)
	require.NoError(t, err)
	require.Equal(t, "test password", password)
	require.Equal(t, 2, env.loadCalls)
}

func TestUnsealSecretUnsupportedFormat(t *testing.T) {
	env := &testEnvironment{
		canEncrypt: true,
		keys: map[string]string{
			"v0-deadbeef-ln-0": base64.StdEncoding.EncodeToString(make([]byte, 32)),
		},
	}
	lightning := newTestLightning(t, env)

	secret, err := lightning.unsealSecret(&config.LightningAccountConfig{
		Code: "v0-deadbeef-ln-0",
	}, "unversioned")
	require.Empty(t, secret)
	require.ErrorContains(t, err, "unsupported encrypted secret format")
}

