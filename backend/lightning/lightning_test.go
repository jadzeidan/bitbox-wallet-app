func TestActivateSealsSecretsWithSingleKey(t *testing.T) {
	env := &testEnvironment{canEncrypt: true}
	lightning := newTestLightning(t, env)
	lightning.getKeystore = testKeystore

	require.NoError(t, lightning.Activate())

	account := lightning.Account()
	require.NotNil(t, account)
	require.True(t, strings.HasPrefix(account.Seed, encryptedSecretV1Prefix))
	require.True(t, strings.HasPrefix(account.WalletPassword, encryptedSecretV1Prefix))
	// One key per account, stored once and reused for both secrets.
	require.Len(t, env.keys, 1)
	require.Contains(t, env.keys, "v0-deadbeef-ln-0")
}

