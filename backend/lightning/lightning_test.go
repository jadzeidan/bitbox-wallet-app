func TestSetStateNotifications(t *testing.T) {
	lightning := newTestLightning(t, nil)
	require.NoError(t, lightning.SetAccount(&config.LightningAccountConfig{
		Seed:            "test mnemonic",
		RootFingerprint: []byte{0xde, 0xad, 0xbe, 0xef},
		Code:            "v0-deadbeef-ln-0",
		Number:          0,
	}))

	var events []observable.Event
	unobserve := lightning.Observe(func(event observable.Event) {
		events = append(events, event)
	})
	defer unobserve()

	subjects := func() []string {
		result := []string{}
		for _, event := range events {
			result = append(result, event.Subject)
		}
		return result
	}

	// First push: readiness flips to true and the payment list is loaded.
	lightning.SetState(&walletState{Ready: true, BalanceSat: 100})
	require.Equal(t, []string{"lightning/ready", "lightning/list-payments"}, subjects())
	require.Equal(t, true, events[0].Object)

	// Identical push: no notifications.
	events = nil
	lightning.SetState(&walletState{Ready: true, BalanceSat: 100})
	require.Empty(t, events)

	// Balance change: only the payment list reloads.
	events = nil
	lightning.SetState(&walletState{Ready: true, BalanceSat: 200})
	require.Equal(t, []string{"lightning/list-payments"}, subjects())

	// New payment: only the payment list reloads.
	events = nil
	lightning.SetState(&walletState{Ready: true, BalanceSat: 200, Payments: []walletPayment{
		{ID: "payment-id", Kind: paymentKindReceive, Status: paymentStatusPending, AmountSat: 100},
	}})
	require.Equal(t, []string{"lightning/list-payments"}, subjects())

	// Readiness lost: only the ready event is emitted.
	events = nil
	lightning.SetState(&walletState{Ready: false, BalanceSat: 200, Payments: []walletPayment{
		{ID: "payment-id", Kind: paymentKindReceive, Status: paymentStatusPending, AmountSat: 100},
	}})
	require.Equal(t, []string{"lightning/ready"}, subjects())
	require.Equal(t, false, events[0].Object)
}

func TestDeactivate(t *testing.T) {
	env := &testEnvironment{canEncrypt: true}
	lightning := newTestLightning(t, env)
	lightning.getKeystore = testKeystore

	require.NoError(t, lightning.Activate())
	lightning.SetState(&walletState{Ready: true, ServerConnected: true})
	require.True(t, lightning.Ready())

	require.NoError(t, lightning.Deactivate())
	require.Nil(t, lightning.Account())
	require.False(t, lightning.Ready())
	// The state cache and the stored encryption key are cleared.
	require.Equal(t, &ServiceStatus{Status: "unknown"}, lightning.ServiceStatus())
	require.Empty(t, env.keys)

	// Deactivating without an account is a no-op.
	require.NoError(t, lightning.Deactivate())
}
